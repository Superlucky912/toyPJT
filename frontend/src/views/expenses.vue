<script>
import { ref } from "vue";

export default {
  name: "Income",

  setup() {
    const year = ref(null);
    const month = ref([]);

    // 멤버 배열에 id와 name을 포함시킵니다
    const members = ref([
      { id: 1, name: "고성빈" },
      { id: 2, name: "오태건" },
      { id: 3, name: "유종훈" },
      { id: 4, name: "임채민" },
      { id: 5, name: "정성희" },
      { id: 6, name: "김예찬" },
    ]);

    // 체크박스 상태를 멤버 ID와 월을 기준으로 관리합니다
    const checkedValues = ref({});

    // 년계산
    year.value = new Date().getFullYear();

    // 월계산 및 체크박스 상태 초기화
    for (let i = 1; i < 13; i++) {
      month.value.push(i + "월");
    }

    // 체크박스 상태 초기화
    members.value.forEach((member) => {
      checkedValues.value[member.id] = {};
      month.value.forEach((m) => {
        checkedValues.value[member.id][m] = false; // 초기값 설정
      });
    });

    return {
      year,
      month,
      members,
      checkedValues,
    };
  },
};
</script>
